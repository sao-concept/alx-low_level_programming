/**
* op_add -This function add two numbers
* @a: parameter1
* @b: parameter2
* Return: result (a + b)
**/
int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub -This function subtract two numbers
* @a: parameter1
* @b: parameter2
* Return: result (a - b)
**/
int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul -This function multiplies two numbers
* @a: parameter1
* @b: parameter2
* Return: result (a * b)
**/

int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div -This function divides two numbers
* @a: parameter1
* @b: parameter2
* Return: result (a / b)
**/

int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod -This function finds modulus of two numbers
* @a: parameter1
* @b: parameter
* Return: result (a % b)
**/

int op_mod(int a, int b)
{
return (a % b);
}
